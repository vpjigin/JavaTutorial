public class DataTypes {
    
    
    public static void main(String[] args){
        
        //How to decalare a datatype
        //{datatype} {identifier} = {value};
        
        
        //byte
        //min_value = -128, max_value = +127
        //8 bit, 1 byte
        //default_value = 0
        byte x = 10;
        byte y = -128;
        byte z = 127;
        
        //short
        //min_value = -32768, max_value = +32767
        //16 bit, 2 byte
        //default_value = 0
        short x1 = 10;
        short x2 = 32767;
        
        //int
        //min_value = -2147483648, max_value = +2147483647
        //32 bit, 4 bytes
        //default_value = 0
        int x3 = 100;
        
        //long
        //min_value = -9223372036854775808, max_value = 9223372036854775807
        //64 bit, 8 bytes
        long x4 = 150000000;
        
        //float
        //32 bit, 4 bytes
        float x5 = 1;
        
        //double
        //64 bit, 8 bytes
        double x6 = 100.2;
        
        //boolean
        //either true or false
        //default_value = false
        boolean flag = true;
        boolean flag1;
        
        //char
        //16 bit, 2 bytes
        char x7 = 'A';
        
        //String
        String testing = "This is a long sentence";
        String name = "Data types";
        
        
        
        
        System.out.println(name);
        
        
    }
    
}
