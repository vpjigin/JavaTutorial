public class SwitchCase {
    
    public static void main(String[] args) {
        
        //switch case
        int x = 3;
        
        switch(x){
            
            case 0:
                System.out.println("Zero");
                break;
                
            case 1:
                System.out.println("One");
                break;
                
            default:
                System.out.println("nothing to print");
                break;
                
        }
        
    }
    
}
